    public Map<String, String> getSearchSelection(){
        Map<String, String> selMap = new HashMap<String, String>();
        if(textProductId.getText() !=null && textProductId.getText().isEmpty()==false && textProductId.getText().length() > 2){
            selMap.put("productId", textProductId.getText().toUpperCase());
        }

        if(textDescription.getText() !=null && textDescription.getText().isEmpty()==false&& textDescription.getText().length() > 2) {
            selMap.put("internalName", textDescription.getText().toUpperCase());
        }  
                
        return selMap;
    }
    
    public void resetSearchSelection(){
        Map<String, String> selMap = new HashMap<String, String>();
        textProductId.setText("");
        textDescription.setText("");

        for (GenericComboSelectionPanel genPanel : filterList) {
           genPanel.setAllSelected();
        }                 
    }
    
